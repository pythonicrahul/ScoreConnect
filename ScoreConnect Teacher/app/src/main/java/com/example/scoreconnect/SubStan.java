package com.example.scoreconnect;

public class SubStan {
    private String Subject_sno;
    private String Subject_name;
    private String Section_sno;
    private String Section;
    private String Standard;
    private String Standard_sno;

    public String getSubject_sno() {
        return Subject_sno;
    }

    public void setSubject_sno(String subject_sno) {
        Subject_sno = subject_sno;
    }

    public String getSubject_name() {
        return Subject_name;
    }

    public void setSubject_name(String subject_name) {
        Subject_name = subject_name;
    }

    public String getSection_sno() {
        return Section_sno;
    }

    public void setSection_sno(String section_sno) {
        Section_sno = section_sno;
    }

    public String getSection() {
        return Section;
    }

    public void setSection(String section) {
        Section = section;
    }

    public String getStandard() {
        return Standard;
    }

    public void setStandard(String standard) {
        Standard = standard;
    }

    public String getStandard_sno() {
        return Standard_sno;
    }

    public void setStandard_sno(String standard_sno) {
        Standard_sno = standard_sno;
    }
}
